	<h2 id = "q3"  class = "tp-header">What is a supplement?</h2>

	<p>A supplement is a request to the insurance company for additional money to
	
	properly repair your vehicle. The insurance initial estimates are just that – an
	
	estimated cost to repair. Dent Control spends a significant amount of time
	
	estimating your vehicle to ensure that we don’t miss anything and repair all damage.
	
	When our estimate exceeds the insurance estimate, we contact them to show them
	
	what they missed. The additional amount is the supplement. Also, if there’s
	
	additional damage found during the repair of your vehicle, an additional supplement
	
	is occasionally needed. Dent Control handles it all for you.</p>
	
