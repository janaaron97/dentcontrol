	<h2 id = "q12" class = "tp-header">How is PDR estimated?</h2>
	<p>Your estimate is based on 3 main factors:<br>
	1. Quantity of dents per panel<br>
	2. Average size of the dents<br>
	3. Location of the dents and difficulty in accessing them.</p><br>
