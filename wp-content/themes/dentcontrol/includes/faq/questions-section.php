	<h2 id = "q4" class = "tp-header">What is a deductible?</h2>

	<p>A deductible is the insured person’s portion of the cost of repairs. It’s the amount of

	money you have to pay toward the repairs before your insurance company covers

	the rest. See the below example:<br>

	$1500 Total Repair Cost<br>

	$500 Deductible (customer pays this amount directly to the repair facility)<br>

	$1000 – Insurance pays this amount. They “deduct” the amount of your deductible<br>

	and pay the rest of the repairs.

	Dent Control can help with your deductible in many cases. <a href = "/contact">Contact us</a> to learn more.</p>
	
