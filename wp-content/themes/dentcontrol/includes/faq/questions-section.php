	<h2 id = "q13" class = "tp-header">How is PDR actually done?</h2>
	<p>PDR is a very tedious process. Using the specialized tools, a PDR Technician pushes the dents out from the backside of the metal. Even the smallest dents (1/4” Diameter) require several push points to completely remove the dent. Dent Control Certified Master Technicians must pass a rigorous training, testing and inspection period prior to working on customers’ vehicles. You can always be assured that your car is in good hands with Dent Control. Only after stringent process of quality inspections can a technician work on your car. Come by one of our locations for a free demonstration.</p>
