	<h2 id = "q14" class = "tp-header">What if I do not have my vehicle repaired?</h2>
	<p>If you make a claim with your insurance and do not have the damage repaired, your insurance company is not responsible for repairs next time your car is damaged by hail. In addition, the amount of damage done by hail will be deducted from any future damage such as a collision on say a fender or door. In some states it is illegal to make a claim and not have the repairs done. Check your local laws for more information.</p>
