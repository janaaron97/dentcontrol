	<h2 id = "q6" class = "tp-header">Will dry ice pop the dents out?</h2>
	<p>Dry ice will not repair a dent on sheet metal. Period. It will, however warp the metal beyond the point of repair with PDR. Usually, a panel that has had dry ice placed on it will have to be replaced.</p>
